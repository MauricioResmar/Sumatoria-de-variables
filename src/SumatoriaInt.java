public class SumatoriaInt
        {
         public static void main(String[] args)
            {
            int num1=3;
            int num2=10;
            int num3=5;

            int resultado = sumatoria(num1,num2,num3);
            System.out.println(resultado);
            }
                public static int sumatoria (int a, int b, int c)
                {
                    int resultado = a+b+c;
                    return resultado;
                }
        }

